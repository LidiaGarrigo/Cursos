<?php

//Operadores Logicos
// > < >= <= <> != ==

$a = 1;
$b = 2;

//Mayor que >
if ($a > $b){
    echo "Verdadero";
} else {
    echo "Incorrecto";
}

echo "<br>";

//Menor que <
if ($a < $b){
    echo "Verdadero";
} else {
    echo "Incorrecto";
}

echo "<br>";

//Mayor o igual que >=
if ($a >= $b){
    echo "Verdadero";
} else {
    echo "Incorrecto";
}

echo "<br>";

//Menor o igual que <=
if ($a <= $b){
    echo "Verdadero";
} else {
    echo "Incorrecto";
}

echo "<br>";

//Diferente <> - !=
if ($a <> $b){
    echo "Verdadero";
} else {
    echo "Incorrecto";
}

echo "<br>";

if ($a != $b){
    echo "Verdadero";
} else {
    echo "Incorrecto";
}

echo "<br>";

//Igual ==
if ($a == $b){
    echo "Verdadero";
} else {
    echo "Incorrecto";
}

?>